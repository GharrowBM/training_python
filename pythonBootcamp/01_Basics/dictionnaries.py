# Les Dictionnaires
my_dictionnary = {"A":"Bernie", 20:"Blackie"}
print(f"my_dictionnary : {my_dictionnary}")
print(f"my_dictionnary[20] : {my_dictionnary[20]}")
my_other_dictionnary = {"key1":["a","b","c","d","e"]}
print(f"my_other_dictionnary : {my_other_dictionnary}")
print(f"my_other_dictionnary['key1'][2].upper() : {my_other_dictionnary['key1'][2].upper()}")
my_other_dictionnary["newKey"] = "NEW VALUE"
print(f"my_other_dictionnary : {my_other_dictionnary}")
print(f"my_dictionnary.keys() : {my_dictionnary.keys()}")
print(f"my_dictionnary.values() : {my_dictionnary.values()}")
print(f"my_dictionnary.items() : {my_dictionnary.items()}")