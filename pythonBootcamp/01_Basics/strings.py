# Les chaines de caract√®re
my_phrase = "Coucou, c'est moi ! Je suis Bernie, ton chien !"
print(f"my_phrase : {my_phrase}")
print(f"my_phrase.split() : {my_phrase.split()}")

# [start:stop:step]
print(f"my_phrase[0:10] : {my_phrase[0:10]}")
print(f"len(my_phrase) : {len(my_phrase)}")

my_dog_name = "Bernie"
print(f"my_dog_name : {my_dog_name}")
print(f"my_dog_name[4] : {my_dog_name[4]}")
print(f"my_dog_name[-2] : {my_dog_name[-2]}")
print(f"my_dog_name.upper() : {my_dog_name.upper()}")
print(f"my_dog_name.lower() : {my_dog_name.lower()}")
